//  UART_Menu_Init(&huart3);
  UART_Menu_Init(&huart3);
  EEPROM_Init(&hi2c1, &huart3);
  I2C_GPIO_InitBus();
  MPR121_Init(&hi2c3, &huart3);
  DWT_Delay_Init();
//  DS18B20_Init();
